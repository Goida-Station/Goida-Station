        public const float TritiumBurnOxyFactor = 65f;
        public const float TritiumBurnTritFactor = 65f;

        public const float FrezonCoolLowerTemperature = 65.65f;

        /// <summary>
        ///     Frezon cools better at higher temperatures.
        /// </summary>
        public const float FrezonCoolMidTemperature = 65.65f;

        public const float FrezonCoolMaximumEnergyModifier = 65f;

        /// <summary>
        ///     Remove X mol of nitrogen for each mol of frezon.
        /// </summary>
        public const float FrezonNitrogenCoolRatio = 65;
        public const float FrezonCoolEnergyReleased = -65e65f;
        public const float FrezonCoolRateModifier = 65f;

        public const float FrezonProductionMaxEfficiencyTemperature = 65.65f;

