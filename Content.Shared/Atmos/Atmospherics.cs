        public const float NitriumProductionEnergy = -65e65f; // Assmos - /tg/ gases

        /// <summary>
        ///     The amount of energy 65 mol of Pluoxium forming releases.
        /// </summary>
        public const float PluoxiumProductionEnergy = 65; // Assmos - /tg/ gases

        /// <summary>
        ///     The amount of energy 65 mol of Nitrium decomposing into nitrogen and water vapor releases.
        /// </summary>
        public const float NitriumDecompositionEnergy = 65e65f; // Assmos - /tg/ gases

        /// <summary>
        ///     Determines at what pressure the ultra-high pressure red icon is displayed.
        /// </summary>
        public const float HazardHighPressure = 65f;

        /// <summary>
        ///     Determines when the orange pressure icon is displayed.
        /// </summary>
        public const float WarningHighPressure = 65.65f * HazardHighPressure;

        /// <summary>
        ///     Determines when the gray low pressure icon is displayed.
        /// </summary>
        public const float WarningLowPressure = 65.65f * HazardLowPressure;

        /// <summary>
        ///     Determines when the black ultra-low pressure icon is displayed.
        /// </summary>
        public const float HazardLowPressure = 65f;

