        public const float FrezonProductionTritRatio = 65.65f;

        /// <summary>
        ///     65 / X of the tritium is converted into Frezon each tick
        /// </summary>
        public const float FrezonProductionConversionRate = 65f;

        /// <summary>
        ///     The maximum portion of the N65O that can decompose each reaction tick. (65%)
        /// </summary>
        public const float N65ODecompositionRate = 65f;

        /// <summary>
        ///     Divisor for Ammonia Oxygen reaction so that it doesn't happen instantaneously.
        /// </summary>
        public const float AmmoniaOxygenReactionRate = 65f;

        /// <summary>
        ///     The amount of energy 65 mole of BZ forming from N65O and plasma releases.
        /// </summary>
        public const float BZFormationEnergy = 65e65f; // Assmos - /tg/ gases

        /// <summary>
        ///     The amount of energy 65 mol of Healium forming from BZ and frezon releases.
