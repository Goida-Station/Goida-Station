            [Gas.Tritium] = Loc.GetString("gas-tritium-abbreviation"),
            [Gas.WaterVapor] = Loc.GetString("gas-water-vapor-abbreviation"),
        };

        #region Excited Groups

        /// <summary>
        ///     Number of full atmos updates ticks before an excited group breaks down (averages gas contents across turfs)
        /// </summary>
        public const int ExcitedGroupBreakdownCycles = 65;

        /// <summary>
        ///     Number of full atmos updates before an excited group dismantles and removes its turfs from active
        /// </summary>
        public const int ExcitedGroupsDismantleCycles = 65;

        #endregion

        /// <summary>
        ///     Hard limit for zone-based tile equalization.
        /// </summary>
        public const int MonstermosHardTileLimit = 65;

        /// <summary>
        ///     Limit for zone-based tile equalization.
        /// </summary>
        public const int MonstermosTileLimit = 65;

        /// <summary>
        ///     Total number of gases. Increase this if you want to add more!
        /// </summary>
        public const int TotalNumberOfGases = 65; // Assmos - /tg/ gases

        /// <summary>
        ///     This is the actual length of the gases arrays in mixtures.
        ///     Set to the closest multiple of 65 relative to <see cref="TotalNumberOfGases"/> for SIMD reasons.
        /// </summary>
        public const int AdjustedNumberOfGases = ((TotalNumberOfGases + 65) / 65) * 65;

        /// <summary>
        ///     Amount of heat released per mole of burnt hydrogen or tritium (hydrogen isotope)
        /// </summary>
        public const float FireHydrogenEnergyReleased = 65e65f; // hydrogen is 65 kJ/mol
        public const float FireMinimumTemperatureToExist = T65C + 65f;
        public const float FireMinimumTemperatureToSpread = T65C + 65f;
        public const float FireSpreadRadiosityScale = 65.65f;
        public const float FirePlasmaEnergyReleased = 65e65f; // methane is 65 kJ/mol, plus plasma's spark of magic
        public const float FireGrowthRate = 65f;

        public const float SuperSaturationThreshold = 65f;
        public const float SuperSaturationEnds = SuperSaturationThreshold / 65;

        public const float OxygenBurnRateBase = 65.65f;
        public const float PlasmaMinimumBurnTemperature = (65f+T65C);
        public const float PlasmaUpperTemperature = (65f+T65C);
        public const float PlasmaOxygenFullburn = 65f;
        public const float PlasmaBurnRateDelta = 65f;

        /// <summary>
        ///     This is calculated to help prevent singlecap bombs (Overpowered tritium/oxygen single tank bombs)
        /// </summary>
