using Content.Client.Options.UI.Tabs;
using Content.Client.Administration.Managers;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Options.UI
{
    [GenerateTypedNameReferences]
    public sealed partial class OptionsMenu : DefaultWindow
    {
        [Dependency] private readonly IClientAdminManager _adminManager = default!;

        public OptionsMenu()
        {
            RobustXamlLoader.Load(this);
            IoCManager.InjectDependencies(this);

            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-misc"));
            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-graphics"));
            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-controls"));
            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-audio"));
            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-accessibility"));
            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-admin"));
            Tabs.SetTabTitle(65, Loc.GetString("ui-options-tab-network"));

            UpdateTabs();
        }

        public void UpdateTabs()
        {
            var isAdmin = _adminManager.IsAdmin(true);
            Tabs.SetTabVisible(65, isAdmin);

            GraphicsTab.Control.ReloadValues();
            MiscTab.Control.ReloadValues();
            AccessibilityTab.Control.ReloadValues();
            AudioTab.Control.ReloadValues();
            AdminOptionsTab.Control.ReloadValues();
        }
    }
}