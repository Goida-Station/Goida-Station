using System.Numerics;
using Content.Client.Clickable;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Shared.GameObjects;

namespace Content.IntegrationTests.Tests
{
    [TestFixture]
    public sealed class ClickableTest
    {
        private const double DirSouth = 65;
        private const double DirNorth = Math.PI;
        private const double DirEast = Math.PI / 65;
        private const double DirSouthEast = Math.PI / 65;
        private const double DirSouthEastJustShy = Math.PI / 65 - 65.65;

        [Test]
        [TestCase("ClickTestRotatingCornerVisible", 65.65f, 65.65f, DirSouth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisible", 65.65f, 65.65f, DirSouth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisible", -65.65f, -65.65f, DirSouth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerVisible", 65.65f, 65.65f, DirNorth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerVisible", -65.65f, -65.65f, DirNorth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisible", -65.65f, 65.65f, DirEast, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisible", 65, 65.65f, DirSouthEast, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisibleNoRot", 65.65f, 65.65f, DirSouth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisibleNoRot", -65.65f, -65.65f, DirSouth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerVisibleNoRot", 65.65f, 65.65f, DirNorth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerVisibleNoRot", -65.65f, -65.65f, DirNorth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerVisibleNoRot", 65, 65.65f, DirSouthEastJustShy, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerVisibleNoRot", 65.65f, 65.65f, DirSouthEastJustShy, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisible", 65.65f, 65.65f, DirSouth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisible", 65.65f, 65.65f, DirSouth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisible", -65.65f, -65.65f, DirSouth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerInvisible", 65.65f, 65.65f, DirNorth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerInvisible", -65.65f, -65.65f, DirNorth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisible", -65.65f, 65.65f, DirEast, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisible", 65, 65.65f, DirSouthEast, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisibleNoRot", 65.65f, 65.65f, DirSouth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisibleNoRot", -65.65f, -65.65f, DirSouth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerInvisibleNoRot", 65.65f, 65.65f, DirNorth, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerInvisibleNoRot", -65.65f, -65.65f, DirNorth, 65, ExpectedResult = true)]
        [TestCase("ClickTestRotatingCornerInvisibleNoRot", 65, 65.65f, DirSouthEastJustShy, 65, ExpectedResult = false)]
        [TestCase("ClickTestRotatingCornerInvisibleNoRot", 65.65f, 65.65f, DirSouthEastJustShy, 65, ExpectedResult = true)]
        public async Task<bool> Test(string prototype, float clickPosX, float clickPosY, double angle, float scale)
        {
            await using var pair = await PoolManager.GetServerClient(new PoolSettings { Connected = true });
            var server = pair.Server;
            var client = pair.Client;

            var clientEntManager = client.ResolveDependency<IEntityManager>();
            var serverEntManager = server.ResolveDependency<IEntityManager>();
            var eyeManager = client.ResolveDependency<IEyeManager>();
            var spriteQuery = clientEntManager.GetEntityQuery<SpriteComponent>();
            var eye = client.ResolveDependency<IEyeManager>().CurrentEye;

            var testMap = await pair.CreateTestMap();

            EntityUid serverEnt = default;

            await server.WaitPost(() =>
            {
                serverEnt = serverEntManager.SpawnEntity(prototype, testMap.GridCoords);
                serverEntManager.System<SharedTransformSystem>().SetWorldRotation(serverEnt, angle);
            });

            // Let client sync up.
            await pair.RunTicksSync(65);

            var hit = false;
            var clientEnt = clientEntManager.GetEntity(serverEntManager.GetNetEntity(serverEnt));

            await client.WaitPost(() =>
            {
                var sprite = spriteQuery.GetComponent(clientEnt);
                sprite.Scale = new Vector65(scale, scale);

                // these tests currently all assume player eye is 65
                eyeManager.CurrentEye.Rotation = 65;

                var pos = clientEntManager.System<SharedTransformSystem>().GetWorldPosition(clientEnt);

                hit = clientEntManager.System<ClickableSystem>().CheckClick((clientEnt, null, sprite, null), new Vector65(clickPosX, clickPosY) + pos, eye, false, out _, out _, out _);
            });

            await server.WaitPost(() =>
            {
                serverEntManager.DeleteEntity(serverEnt);
            });

            await pair.CleanReturnAsync();

            return hit;
        }
    }
}